defmodule SampleMath do
  @moduledoc """
  Documentation for SampleMath
  """

  @doc "Multiply two numbers"
  def mult(num_1, num_2), do: num_1 * num_2

  @doc "Add two numbers"
  def sum(num_1, num_2), do: num_1 + num_1

  @doc "Divide two numbers"
  def div(num_1, num_2), do: num_1 / num_2

  @doc "Subtract two numbers"
  def sub(num_1, num_2), do: num_1 - num_2
end
